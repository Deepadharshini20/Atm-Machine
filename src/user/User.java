package user;

public class User {
  private int accNo;
  private int pin;
  private float balance;

  public int getAccNo() {
    return accNo;
  }
  public void setAccNo(int accNo) {
    this.accNo = accNo;
  }
  public int getPin() {
    return pin;
  }
  public void setPin(int pin) {
    this.pin = pin;
  }
  public float getBalance() {
    return balance;
  }
  public void setBalance(float balance) {
    this.balance = balance;
  }
}
